#include <stdio.h>


/**
 * add - add two integers
 * @a: first unit
 * @b: second unit
 * Return: addition
 */
int add (int a, int b)
{
	return (a + b);
}

/**
 * sub - subtract two integers
 * @a: first int
 * @b: second unit
 * Return: subtracted final result
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies two integers
 * @a: first int
 * @b: second int
 * Return: multiplied value
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides two integers
 * @a: first int
 * @b: second int
 * Return: divided value
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - find the modulus of two integers
 * @a: first int
 * @b: second int
 * Return: modulated value
 */
int mod(int a, int b)
{
	return (a % b);
}
